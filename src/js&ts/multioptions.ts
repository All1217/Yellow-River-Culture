// 随机生成颜色的函数
function getRandomColor(): string {
    const letters = '0123456789ABCDEF';
    let color = '#';
    for (let i = 0; i < 6; i++) {
        color += letters[Math.floor(Math.random() * 16)];
    }
    return color;
}
// 生成带有随机颜色的三维数据的函数
function generateRandomData(numPoints: number[][]) {
    const data = [];
    numPoints.forEach(e => {
        const x = e[0];
        const y = e[1];
        let size = e[2];
        if (size < 1) {
            size = 1;
        } else if (size > 10) {
            size = 6;
        }
        data.push({
            value: [y, x, size],
            itemStyle: {
                borderColor: getRandomColor(),
                borderWidth: 2,
                color: 'transparent'
            }
        });
    })
    return data;
}